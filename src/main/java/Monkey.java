public class Monkey {
    private int x = (int) Math.random() * 100;
    private int y = (int) Math.random() * 100;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
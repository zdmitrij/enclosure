public class Monkey {
    public int x = (int) (Math.random() * 10);
    public int y = (int) (Math.random() * 10);

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}